// random and ceil, floor methods
// Math.random() method outputs the number between 0 and 0.9999999999999999
// Math.ceil() method outputs the number rounded up to the nearest integer
// Math.floor() method outputs the number rounded down to the nearest integer
var random = Math.random();
random = random * 6;
var ceil = Math.ceil(random);
var floor = Math.floor(random);
console.log("The upper number" ,ceil);
console.log("The lower number" ,floor);



